DROP TABLE IF EXISTS chat;
DROP TABLE IF EXISTS TherapeutPatient;
DROP TABLE IF EXISTS kontakte;
DROP TABLE IF EXISTS orte;
DROP TABLE IF EXISTS token;
DROP TABLE IF EXISTS unterstuetzung;
DROP TABLE IF EXISTS `user`;
DROP TABLE IF EXISTS frageboegen;
DROP TABLE IF EXISTS fragebogen_zuweisung;
DROP TABLE IF EXISTS frageboegen_abgeschlossen;
DROP TABLE IF EXISTS param;


CREATE TABLE chat
(
   id          BIGINT        NOT NULL AUTO_INCREMENT,
   nachricht   TEXT,
   author      VARCHAR(36),
   empfaenger  VARCHAR(36),
   erstellt    DATETIME,
   gelesen     BIT           DEFAULT b'0',
   PRIMARY KEY (id)
);


CREATE TABLE TherapeutPatient
(
   therapeutUUID  VARCHAR(36)   NOT NULL,
   patientUUID    VARCHAR(36)   NOT NULL,
   PRIMARY KEY (therapeutUUID, patientUUID)
);


CREATE TABLE kontakte
(
   id      INT            NOT NULL AUTO_INCREMENT,
   name    VARCHAR(255),
   art     VARCHAR(35),
   email   VARCHAR(255),
   phone   VARCHAR(35),
   userID  VARCHAR(36),
   PRIMARY KEY (id)
);


CREATE TABLE orte
(
   id            INT            NOT NULL AUTO_INCREMENT,
   patient_uuid  VARCHAR(36),
   titel         VARCHAR(255),
   beschreibung  TEXT,
   lat           VARCHAR(20),
   lng           VARCHAR(20),
   PRIMARY KEY (id)
);


CREATE TABLE token
(
   token       VARCHAR(255)   NOT NULL,
   expiration  DATETIME,
   PRIMARY KEY (token)
);


CREATE TABLE unterstuetzung
(
   id          INT            NOT NULL AUTO_INCREMENT,
   titel       VARCHAR(255),
   text        MEDIUMBLOB,
   author      VARCHAR(36),
   empfaenger  VARCHAR(36),
   PRIMARY KEY (id)
);


CREATE TABLE `user`
(
   uuid         VARCHAR(36)      NOT NULL,
   vorname      VARCHAR(255),
   nachname     VARCHAR(255),
   email        VARCHAR(255),
   therapeut    BIT,
   passwort     VARCHAR(255),
   username     VARCHAR(255),
   Lat          DECIMAL(65,30),
   Lng          DECIMAL(65,30),
   techuser     BIT,
   schwellwert  INT,
   PRIMARY KEY (uuid)
);

CREATE TABLE frageboegen
(
   id            BIGINT        NOT NULL AUTO_INCREMENT,
   titel         TEXT,
   beschreibung  TEXT,
   author        VARCHAR(36),
   json          MEDIUMBLOB,
   PRIMARY KEY (id)
);

CREATE TABLE fragebogen_zuweisung
(
   id              BIGINT        NOT NULL AUTO_INCREMENT,
   frageboegen_id  BIGINT,
   empfaenger      VARCHAR(36),
   cron            TEXT,
   erstellt        DATE,
   PRIMARY KEY (id)
);

CREATE TABLE frageboegen_abgeschlossen
(
   id            BIGINT        NOT NULL AUTO_INCREMENT,
   titel         TEXT,
   beschreibung  TEXT,
   patient_uuid  VARCHAR(36),
   ergebnis      MEDIUMBLOB,
   wert          INT,
   erstellt      DATE,
   PRIMARY KEY (id)
);

CREATE TABLE param
(
   integer_value  INT,
   boolean_value  BIT,
   string_value   MEDIUMTEXT,
   param          VARCHAR(25)   NOT NULL,
   PRIMARY KEY (param)
);

